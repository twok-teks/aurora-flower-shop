let language = localStorage.getItem("aurora-language") === "en" ? "en" : "vi";

export function t(key) {
  return translations[language][key] ?? translations.vi[key] ?? key;
}

export function getLanguage() {
  return language;
}

export function translateCategory(category) {
  const key = `category.${category}`;
  return translations[language][key] ?? translations.vi[key] ?? category;
}

function applyLanguage() {
  document.documentElement.lang = language;
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(element.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    element.setAttribute("aria-label", t(element.dataset.i18nAriaLabel));
  });
  document.querySelectorAll("[data-language-toggle]").forEach((button) => {
    button.textContent = language === "vi" ? "EN" : "VI";
    button.setAttribute("aria-label", t("lang.switch"));
    button.setAttribute("title", t("lang.switch"));
  });
  const page = document.body.dataset.page;
  const titles = {
    home: language === "vi" ? "Aurora | Tiệm hoa tại TP. Hồ Chí Minh" : "Aurora Flower Shop | Ho Chi Minh City",
    shop: language === "vi" ? "Cửa hàng | Aurora" : "Shop Flowers | Aurora",
    contact: language === "vi" ? "Liên hệ | Aurora" : "Contact | Aurora"
  };
  if (titles[page]) document.title = titles[page];
}

export function initLanguage() {
  applyLanguage();
  document.querySelectorAll("[data-language-toggle]").forEach((button) => {
    button.addEventListener("click", () => {
      language = language === "vi" ? "en" : "vi";
      localStorage.setItem("aurora-language", language);
      applyLanguage();
      document.dispatchEvent(new CustomEvent("aurora:languagechange", { detail: { language } }));
    });
  });
}
